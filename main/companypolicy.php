<h3 style="color:red">Placement Policy:</h3>
                            • The Students' Placement Office follows a strict "One Student, One Job Policy". Once a student is offered a job, he/she is out of the placement process</br>
• On completion of the recruitment procedure, the company needs to submit the final list of offers in a closed envelope to the placement office. The offers will be opened at the end of the slot when all companies finish their process</br>
• No "On the Spot" offers should be made nor can any cue be given to any of the candidates during the placement interview.</br>
• In case a candidate receives multiple offers, the candidate has to make a choice at the end of the slot. The companies will be intimated about the   final choice of the candidate two hours after the end of the slot, or receipt of the offers, whichever is later.</br>
• Since there is a high probability of multiple offers, the companies have to furnish a waiting list to the placement office along with the final offers. In case a student who is extended multiple offers chooses the other company, the first candidate in the waiting list moves up in the final offer list. The waitlist will not be disclosed to the students and will be released on a per student basis</br>
• Companies are advised to be in touch with the Placement volunteers and staff at all times. They should not entertain any direct contact from the students appearing in the placement process. In case of such a situation, the company must bring this immediately to the notice of the Placement Office</br>
